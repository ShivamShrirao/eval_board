import Link from "next/link";
import { notFound } from "next/navigation";
import type { Metadata } from "next";
import { getModelDetail } from "../../../lib/server/model-service";
import { ModelGallery } from "../../../components/models/model-gallery";

export const dynamic = "force-dynamic";
export const revalidate = 0;

interface ModelPageProps {
  params: Promise<{
    id: string;
  }>;
}

export async function generateMetadata({ params }: ModelPageProps): Promise<Metadata> {
  const { id } = await params;
  const model = await getModelDetail(id);
  if (!model) {
    return {
      title: "Model not found • Eval Board"
    };
  }

  return {
    title: `${model.name} • Models • Eval Board`,
    description: model.description ?? `Images generated by ${model.name}`
  };
}

export default async function ModelDetailPage({ params }: ModelPageProps) {
  const { id } = await params;
  const model = await getModelDetail(id);

  if (!model) {
    notFound();
  }

  return (
    <div className="mx-auto flex w-full max-w-6xl flex-col gap-8 px-6 py-10">
      <div className="flex flex-col gap-6 rounded-2xl border border-slate-900 bg-black/70 p-6 shadow-xl shadow-black/40">
        <div className="flex flex-wrap items-start justify-between gap-4">
          <div>
            <p className="text-xs uppercase tracking-[0.2em] text-slate-500">Model</p>
            <h1 className="text-3xl font-semibold text-slate-100">{model.name}</h1>
            {model.description ? <p className="mt-2 text-slate-400">{model.description}</p> : null}
            <p className="mt-2 text-sm text-slate-500">Slug: {model.slug}</p>
          </div>
          <Link
            href="/?tab=models"
            className="inline-flex items-center gap-2 rounded-full border border-slate-800/70 bg-slate-900/80 px-4 py-2 text-sm font-medium text-slate-200 transition hover:border-slate-500 hover:text-white"
          >
            ← Back to models
          </Link>
        </div>
        <dl className="grid gap-4 text-sm text-slate-300 sm:grid-cols-3">
          <div className="rounded-xl border border-slate-900/60 bg-black/40 p-4">
            <dt className="text-xs uppercase tracking-wide text-slate-500">Datasets</dt>
            <dd className="mt-1 text-lg font-semibold text-slate-100">{model.datasetCount}</dd>
          </div>
          <div className="rounded-xl border border-slate-900/60 bg-black/40 p-4">
            <dt className="text-xs uppercase tracking-wide text-slate-500">Images</dt>
            <dd className="mt-1 text-lg font-semibold text-slate-100">{model.imageCount}</dd>
          </div>
          <div className="rounded-xl border border-slate-900/60 bg-black/40 p-4">
            <dt className="text-xs uppercase tracking-wide text-slate-500">Created</dt>
            <dd className="mt-1 text-base text-slate-200">{new Date(model.createdAt).toLocaleString()}</dd>
          </div>
        </dl>
      </div>

      <div className="rounded-2xl border border-slate-900 bg-black/60 p-6 shadow-inner shadow-black/40">
        <div className="mb-4 flex items-center justify-between">
          <h2 className="text-lg font-semibold text-slate-100">Image gallery</h2>
          <p className="text-sm text-slate-500">{model.imageCount} artifacts</p>
        </div>
        <ModelGallery modelId={model.id} />
      </div>
    </div>
  );
}
